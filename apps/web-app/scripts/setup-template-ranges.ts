const RSK_TEXT_ANCHORS: TextAnchor[] = [
  { name: 'f_nasabah', kind: 'bracket', prefix: '[Nama Perusahaan / Individu]' },
  { name: 'f_akad', kind: 'bracket', prefix: '[Musyarakah / Murabahah / Ijarah / Istishna / dll.]' },
  { name: 'f_tenor', kind: 'exact', text: '[X] bulan sejak penandatanganan akad' },
  // RSK risk prose (per aspect) is wired through the matrix locator above —
  // the matrix already creates `<asp>_finding` / `<asp>_mitigation` NamedRanges
  // around the matrix cells. No extra anchors needed for risk prose.
  // f_plafond is intentionally absent: positionally fragile on the RSK template.
]

// Locate every TextAnchor that doesn't yet have a NamedRange. Falls back to
// the legacy `{{name}}` literal if the original locator doesn't match — this
// is the migration path for masters that already went through the old tokens
// script (the bracket / exact source string is already gone, replaced by `{{name}}`).
function textPlacements(doc: docs_v1.Schema$Document, anchors: TextAnchor[]): Placement[] {
  const cm = buildCharMap(doc.body?.content ?? undefined)
  const existing = new Set(Object.keys(doc.namedRanges ?? {}))
  const out: Placement[] = []
  for (const a of anchors) {
    if (existing.has(a.name)) continue
    // 1. Legacy token literal — wins if present (the master was already tokenized once).
    const litIdx = cm.full.indexOf(`{{${a.name}}}`)
    if (litIdx >= 0) {
      const start = cm.at[litIdx]
      const end = cm.at[litIdx + `{{${a.name}}}`.length - 1] + 1
      out.push({ marker: a.name, start, end })
      continue
    }
    // 2. Original locator (bracket / exact).
    if (a.kind === 'bracket') {
      const i = cm.full.indexOf(a.prefix)
      if (i < 0) continue
      const close = cm.full.indexOf(']', i + a.prefix.length - 1)
      if (close < 0) continue
      const start = cm.at[i]
      const end = cm.at[close] + 1
      out.push({ marker: a.name, start, end })
    } else if (a.kind === 'exact') {
      const i = cm.full.indexOf(a.text)
      if (i < 0) continue
      const start = cm.at[i]
      const end = cm.at[i + a.text.length - 1] + 1
      out.push({ marker: a.name, start, end })
    }
    // 'token-lit' branch is handled above unconditionally — kept in the type for
    // explicitness; no separate case needed.
  }
  return out
}

// ── two-pass placement ───────────────────────────────────────────────────────
const WHITE = { color: { rgbColor: { red: 1, green: 1, blue: 1 } } }
const HIDDEN = { foregroundColor: WHITE, fontSize: { magnitude: 1, unit: 'PT' } }

async function setupDoc(
  docs: docs_v1.Docs,
  documentId: string,
  label: string,
  placements: Placement[],
): Promise<void> {
  const before = await docs.documents.get({ documentId })
  const cm0 = buildCharMap(before.data.body?.content ?? undefined)

  if (placements.length === 0) {
    console.log(`\n[${label}] ⚠ no fields located — check template structure.`)
    return
  }
  // Per-marker idempotency: only place markers not already present.
  const todo = placements.filter((p) => !findToken(cm0, sentinelStart(p.marker)))
  if (todo.length === 0) {
    console.log(`\n[${label}] all ${placements.length} markers already present — skipping.`)
    return
  }

  // Pass 1: insert ${{name}} … ${{/name}} tokens around each value. Empty cell →
  // one combined token with a "-" placeholder so a NamedRange can exist.
  const inserts: { index: number; text: string }[] = []
  for (const p of todo) {
    if (p.end > p.start) {
      inserts.push({ index: p.end, text: ` ${sentinelEnd(p.marker)}` })
      inserts.push({ index: p.start, text: `${sentinelStart(p.marker)} ` })
    } else {
      inserts.push({ index: p.start, text: `${sentinelStart(p.marker)} - ${sentinelEnd(p.marker)}` })
    }
  }
  inserts.sort((a, b) => b.index - a.index) // descending: lower indices stay valid
  await docs.documents.batchUpdate({
    documentId,
    requestBody: { requests: inserts.map((i) => ({ insertText: { location: { index: i.index }, text: i.text } })) },
  })

  // Pass 2: locate tokens by text, create NamedRanges + hide the token spans.
  const after = await docs.documents.get({ documentId })
  const cm = buildCharMap(after.data.body?.content ?? undefined)
  const requests: docs_v1.Schema$Request[] = []
  let placed = 0
  const failed: string[] = []
  for (const p of todo) {
    const s = findToken(cm, sentinelStart(p.marker))
    const e = s ? findToken(cm, sentinelEnd(p.marker), s.offset + sentinelStart(p.marker).length) : null
    if (!s || !e) {
      failed.push(p.marker)
      continue
    }
    if (e.start > s.end) {
      requests.push({ createNamedRange: { name: p.marker, range: { startIndex: s.end, endIndex: e.start } } })
    }
    requests.push({ updateTextStyle: { range: { startIndex: s.start, endIndex: s.end }, textStyle: HIDDEN, fields: 'foregroundColor,fontSize' } })
    requests.push({ updateTextStyle: { range: { startIndex: e.start, endIndex: e.end }, textStyle: HIDDEN, fields: 'foregroundColor,fontSize' } })
    placed++
  }
  await docs.documents.batchUpdate({ documentId, requestBody: { requests } })
  console.log(`\n[${label}] placed ${placed}/${todo.length} new markers (of ${placements.length} total).`)
  if (failed.length) console.log(`  ✗ failed: ${failed.join(', ')}`)
}

// Pass B: pick up any hand-authored tokens not covered by structural/text anchors.
// Paired `${{X}} … ${{/X}}` → NamedRange over the content (tokens hidden).
// Lone `${{X}}` → NamedRange over the token itself (kept visible; analyst replaces it).
async function scanHandAuthoredTokens(docs: docs_v1.Docs, documentId: string, label: string): Promise<void> {
  const doc = (await docs.documents.get({ documentId })).data
  const cm = buildCharMap(doc.body?.content ?? undefined)
  const existing = new Set(Object.keys(doc.namedRanges ?? {}))

  const re = /\$\{\{(\/?)(\w+)\}\}/g
  const toks: { kind: 'open' | 'close'; name: string; start: number; end: number }[] = []
  for (let m = re.exec(cm.full); m; m = re.exec(cm.full)) {
    const name = m[2]
    if (!name) continue
    toks.push({ kind: m[1] ? 'close' : 'open', name, start: cm.at[m.index], end: cm.at[m.index + m[0].length - 1] + 1 })
  }

  const requests: docs_v1.Schema$Request[] = []
  const created: string[] = []
  const usedClose = new Set<number>()
  const hide = (s: number, e: number): docs_v1.Schema$Request => ({
    updateTextStyle: { range: { startIndex: s, endIndex: e }, textStyle: HIDDEN, fields: 'foregroundColor,fontSize' },
  })
  toks.forEach((t, i) => {
    if (t.kind !== 'open' || existing.has(t.name)) return
    let close: (typeof toks)[number] | undefined
    for (let j = i + 1; j < toks.length; j++) {
      if (toks[j].kind === 'close' && toks[j].name === t.name && !usedClose.has(j)) {
        close = toks[j]
        usedClose.add(j)
        break
      }
    }
    if (close && close.start > t.end) {
      requests.push({ createNamedRange: { name: t.name, range: { startIndex: t.end, endIndex: close.start } } })
      requests.push(hide(t.start, t.end), hide(close.start, close.end))
    } else {
      requests.push({ createNamedRange: { name: t.name, range: { startIndex: t.start, endIndex: t.end } } })
    }
    created.push(t.name)
    existing.add(t.name)
  })

  if (requests.length) await docs.documents.batchUpdate({ documentId, requestBody: { requests } })
  if (created.length) console.log(`[${label}] generic scan: created ${created.length} hand-authored marker(s): ${created.join(', ')}`)
}

async function main() {
  const docs = docsClient()
  const rsk = process.env.GOOGLE_MASTER_RSK_DOC_ID
  const muap = process.env.GOOGLE_MASTER_MUAP_DOC_ID
  if (!rsk || !muap) throw new Error('Missing GOOGLE_MASTER_{RSK,MUAP}_DOC_ID — run create-masters first.')

  const rskDoc = (await docs.documents.get({ documentId: rsk })).data
  await setupDoc(docs, rsk, 'RSK matrix', matrixPlacements(rskDoc))
  // Re-fetch — structural pass mutated the doc, indices have moved.
  const rskAfterMatrix = (await docs.documents.get({ documentId: rsk })).data
  await setupDoc(docs, rsk, 'RSK text fields', textPlacements(rskAfterMatrix, RSK_TEXT_ANCHORS))
  await scanHandAuthoredTokens(docs, rsk, 'RSK')

  const muapDoc = (await docs.documents.get({ documentId: muap })).data
  await setupDoc(docs, muap, 'MUAP ratios + collateral', [...ratioPlacements(muapDoc), ...collateralPlacements(muapDoc)])
  const muapAfterStructural = (await docs.documents.get({ documentId: muap })).data
  await setupDoc(docs, muap, 'MUAP text fields', textPlacements(muapAfterStructural, MUAP_TEXT_ANCHORS))
  await scanHandAuthoredTokens(docs, muap, 'MUAP')

  console.log(`\nExpected — matrix: ${MATRIX_ASPECTS.length * 3}, ratios: ${RATIO_KEYS.length * 3} + 3 periods, collateral: 3, MUAP text: ${MUAP_TEXT_ANCHORS.length}, RSK text: ${RSK_TEXT_ANCHORS.length}.`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
