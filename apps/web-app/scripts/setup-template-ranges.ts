/**
 * Place extraction markers on the master Docs: wrap each field's value with hidden
 * `${{name}}…${{/name}}` sentinels (1pt white) AND create a NamedRange over the
 * value between them. The NamedRange is the SINGLE primitive used for both
 * directions at runtime:
 *   • fill (server/docs/seed.ts)    → `replaceNamedRangeContent` writes the value
 *   • read (server/google/extract)  → text between the sentinels = the value
 *
 * This replaces the old two-script split (ranges for extraction + `{{token}}`
 * literals for fill). The `{{token}}` mechanism is GONE — `setup-template-tokens.ts`
 * is deleted. Re-running this script against a master that still carries old
 * `{{token}}` literals will wrap them with sentinels + create the NamedRange in
 * place; first seed then writes the real content via `replaceNamedRangeContent`.
 *
 * Locators (per anchor):
 *   • structural  — table-cell discovery (RSK risk matrix, MUAP ratio + collateral)
 *   • bracket     — find `[guidance…]` by unique prefix, slice through closing `]`
 *   • exact       — find a unique literal (e.g. "Rp ____________,-")
 *   • token-lit   — find a bare `{{name}}` literal left by the old tokens script
 *   • generic     — scan for hand-authored `${{X}}…${{/X}}` / `${{X}}` already typed
 *
 * Idempotent at the marker-name granularity: a name that already has a NamedRange
 * is skipped, so adding new anchors to a partially-set-up master is safe.
 *
 * COMPLIANCE: anchors never target the RSK risk-matrix LEVELS (`_level` is auto-
 * generated by the matrix locator and lives in extract.ts as a read-only slot;
 * seed.ts' FORBIDDEN_KEY regex is the runtime defence) nor the decision RECOMMENDATION.
 *
 * Run:  pnpm exec tsx apps/web-app/scripts/setup-template-ranges.ts
 */
