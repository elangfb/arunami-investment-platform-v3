// ── doc helpers ──────────────────────────────────────────────────────────────
function cellRuns(cell: docs_v1.Schema$TableCell): TextRun[] {
  const out: TextRun[] = []
  collectRuns(cell.content ?? undefined, out)
  return out
}
function cellText(cell: docs_v1.Schema$TableCell | undefined): string {
  if (!cell) return ''
  return cellRuns(cell).map((r) => r.text).join('').replace(/\s+/g, ' ').trim()
}
// Index range of a cell's text, excluding the trailing paragraph newline.
function cellBounds(cell: docs_v1.Schema$TableCell | undefined): { start: number; end: number } | null {
  if (!cell) return null
  const runs = cellRuns(cell)
  if (!runs.length) return null
  const start = runs[0].start
  const last = runs[runs.length - 1]
  let end = last.end
  if (last.text.endsWith('\n')) end -= 1
  return { start, end }
}

// Like cellBounds but stops the range before `stopBefore` (e.g. wrap "___%" but
// not the trailing "(min. 80%)" annotation). Trims trailing whitespace.
function valueBounds(cell: docs_v1.Schema$TableCell | undefined, stopBefore?: string): { start: number; end: number } | null {
  if (!cell) return null
  const cm = buildCharMap(cell.content ?? undefined)
  if (!cm.at.length) return null
  let end = cm.full.length
  if (stopBefore) {
    const i = cm.full.indexOf(stopBefore)
    if (i >= 0) end = i
  }
  while (end > 0 && /\s/.test(cm.full[end - 1])) end--
  if (end <= 0) return null
  const start = cm.at[0]
  const endDoc = cm.at[end - 1] + 1
  return endDoc > start ? { start, end: endDoc } : null
}
function tables(doc: docs_v1.Schema$Document): docs_v1.Schema$Table[] {
  return (doc.body?.content ?? []).flatMap((el) => (el.table ? [el.table] : []))
}

// ── structural locators (table-aware) ────────────────────────────────────────
interface Placement { marker: string; start: number; end: number }

const ASPECT_BY_LABEL: Record<string, MatrixAspect> = {
  CHARACTER: 'character',
  CAPACITY: 'capacity',
  CAPITAL: 'capital',
  COLLATERAL: 'collateral',
  CONDITION: 'condition',
  'SHARIA COMPLIANCE': 'sharia_compliance',
  'SHARIA STRUCTURING': 'sharia_structuring',
}

function matrixPlacements(doc: docs_v1.Schema$Document): Placement[] {
  const table = tables(doc).find((t) =>
    (t.tableRows ?? []).some((r) => ASPECT_BY_LABEL[cellText(r.tableCells?.[0]).toUpperCase()]),
  )
  if (!table) return []
  const out: Placement[] = []
  for (const row of table.tableRows ?? []) {
    const cells = row.tableCells ?? []
    const aspect = ASPECT_BY_LABEL[cellText(cells[0]).toUpperCase()]
    if (!aspect) continue
    // cols: 0 Dimensi | 1 Temuan(finding) | 2 Level | 3 Mitigasi
    const map: [string, number][] = [
      [`${aspect}_finding`, 1],
      [`${aspect}_level`, 2],
      [`${aspect}_mitigation`, 3],
    ]
    for (const [marker, col] of map) {
      const b = cellBounds(cells[col])
      if (b) out.push({ marker, ...b })
    }
  }
  return out
}

const RATIO_BY_LABEL: { re: RegExp; key: RatioKey }[] = [
  { re: /current ratio/i, key: 'currentRatio' },
  { re: /debt to equity|\bder\b/i, key: 'der' },
  { re: /gross profit margin|\bgpm\b/i, key: 'gpm' },
  { re: /net profit margin|\bnpm\b/i, key: 'npm' },
  { re: /dscri/i, key: 'dscri' },
]

function ratioPlacements(doc: docs_v1.Schema$Document): Placement[] {
  const table = tables(doc).find(
    (t) =>
      cellText(t.tableRows?.[0]?.tableCells?.[0]).toUpperCase() === 'RASIO' &&
      (t.tableRows?.[0]?.tableCells?.length ?? 0) >= 5,
  )
  if (!table) return []
  const rows = table.tableRows ?? []
  const out: Placement[] = []
  // period headers: row 0, cols 1..3
  const hdr = rows[0]?.tableCells ?? []
  for (const i of [0, 1, 2]) {
    const b = cellBounds(hdr[i + 1])
    if (b) out.push({ marker: `fin_period_${i}`, ...b })
  }
  // ratio rows: cols 1..3 are the period values
  for (const row of rows.slice(1)) {
    const cells = row.tableCells ?? []
    const match = RATIO_BY_LABEL.find((m) => m.re.test(cellText(cells[0])))
    if (!match) continue
    for (const i of [0, 1, 2]) {
      const b = cellBounds(cells[i + 1])
      if (b) out.push({ marker: `ratio_${match.key}_${i}`, ...b })
    }
  }
  return out
}

// MUAP §VII agunan summary: TOTAL row (market value / CEV) + SCCR % row.
function collateralPlacements(doc: docs_v1.Schema$Document): Placement[] {
  const table = tables(doc).find((t) => {
    const r0 = (t.tableRows?.[0]?.tableCells ?? []).map((c) => cellText(c))
    return r0.some((c) => /nilai pasar/i.test(c)) && r0.some((c) => /\bcev\b/i.test(c))
  })
  if (!table) return []
  const rows = table.tableRows ?? []
  const out: Placement[] = []
  const total = rows.find((r) => cellText(r.tableCells?.[1]).toUpperCase() === 'TOTAL')
  if (total) {
    const mv = cellBounds(total.tableCells?.[3])
    if (mv) out.push({ marker: 'collateral_market_value', ...mv })
    const lv = cellBounds(total.tableCells?.[5])
    if (lv) out.push({ marker: 'collateral_liquidation_value', ...lv })
  }
  const sccr = rows.find((r) => /^sccr/i.test(cellText(r.tableCells?.[1])))
  if (sccr) {
    const b = valueBounds(sccr.tableCells?.[5], ' (') // exclude "(min. 80%)"
    if (b) out.push({ marker: 'collateral_sccr_pct', ...b })
  }
  return out
}

// ── text locators (bracket / exact / token-lit) ──────────────────────────────
// Each entry yields a Placement by locating a unique slice of the doc's
// concatenated text. Ordering inside the list matters only for tie-breaking;
// per-name idempotency runs at the placement level.
type TextAnchor =
  | { name: string; kind: 'bracket'; prefix: string }      // [guidance…] by unique prefix
  | { name: string; kind: 'exact'; text: string }          // unique literal in the doc
  | { name: string; kind: 'token-lit' }                    // legacy {{name}} from old tokens script

